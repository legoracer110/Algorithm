	static int harvest(int[][] map, int size) {
		int answer = 0;

		for (int i = 0; i <= size / 2; i++) {
			for (int j = size / 2 - i; j <= size / 2 + i; j++) {
				answer += map[i][j];
			}
		}

		for (int i = size / 2 + 1; i < size; i++) {
			int tmp = size - i - 1;
			for (int j = size / 2 - tmp; j <= size / 2 + tmp; j++) {
				answer += map[i][j];
			}
		}

		return answer;
	}

